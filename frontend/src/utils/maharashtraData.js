// Get all district names as array
export const getDistricts = () => {
  return Object.keys(maharashtraDistricts).sort()
}

// Get talukas for a specific district
export const getTalukas = (district) => {
  if (!district || !maharashtraDistricts[district]) {
    return []
  }
  return maharashtraDistricts[district].sort()
}
